<script setup>
if (process.client) { // Ensure the code only runs in the browser
  // Run code after Nuxt has mounted
  onMounted(() => {
    console.log("Accordion plugin loaded");

    // Find the accordion element
    const faqAccordion = document.getElementById("faqAccordion");

    if (faqAccordion) { // Ensure the element exists
      faqAccordion.addEventListener("hide.bs.collapse", (event) => {
        const iframe = event.target.querySelector("iframe");
        if (iframe) {
          // Stop the video by resetting the iframe's src
          const src = iframe.src;
          iframe.src = ""; // Remove src to stop the video
          iframe.src = src; // Reset src to reload the video
          console.log("Video stopped");
        }
      });

      faqAccordion.addEventListener("show.bs.collapse", (event) => {
        const iframe = event.target.querySelector("iframe");
        if (iframe) {
          // Add autoplay and mute parameters dynamically
          const url = new URL(iframe.src);
          url.searchParams.set("autoplay", "1");
          url.searchParams.set("mute", "1");
          iframe.src = url.toString();
          console.log("Video started with autoplay and mute");
        }
      });
    } else {
      console.error("faqAccordion element not found");
    }
  });
}
</script>

